package com.github.javydreamercsw.management.domain.campaign;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Entity
@Table(name = "campaign_upgrade")
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class CampaignUpgrade {

  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY)
  private Long id;

  @Column(name = "name", nullable = false)
  private String name;

  @Column(name = "description", nullable = false, length = 1000)
  private String description;

  @Column(name = "type", nullable = false)
  private String type; // e.g., STAMINA, HEALTH, DAMAGE, DEFENSE

  @Column(name = "sub_type")
  private String subType; // e.g., Strike, Grapple, Throw, Aerial for DAMAGE type
}
